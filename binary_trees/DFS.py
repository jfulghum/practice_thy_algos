# ⬛️ Recursive Solution
# ▔▔▔▔▔▔▔▔▔▔▔▔▔▔▔▔▔▔▔▔▔▔▔▔▔▔▔▔▔▔▔▔▔▔▔▔▔▔▔▔▔▔▔▔▔▔▔▔▔▔▔▔▔▔▔▔▔▔▔▔
def find_dfs_rec(node, target):
    if not node:
        return False

    if node.value == target:
        return True

    return find_dfs_rec(node.left, target) or find_dfs_rec(node.right, target)
